export function AccountSidebar() {
  const pathname = usePathname()

  function isActive(item: NavItem) {
    return item.exact ? pathname === item.href : pathname.startsWith(item.href)
  }

  return (
    <nav className="bg-white rounded-card2 shadow-card p-2 sm:p-3 flex flex-row sm:flex-col gap-1 overflow-x-auto">
      {NAV.map(item => {
        const active = isActive(item)
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`flex items-center gap-3 px-3 py-2.5 rounded-xl font-sans text-sm whitespace-nowrap transition-colors ${
              active
                ? 'bg-brand/10 text-brand font-semibold'
                : 'text-ink/80 hover:bg-surface hover:text-ink'
            }`}
            aria-current={active ? 'page' : undefined}
          >
            <span className={active ? 'text-brand' : 'text-muted'}>{item.icon}</span>
            {item.label}
          </Link>
        )
      })}

      {/* Sign out — visually grouped with the nav, like the reference. */}
      <div className="flex items-center gap-3 px-3 py-2.5 rounded-xl hover:bg-surface transition-colors">
        <span className="text-muted">
          <svg className="w-5 h-5" fill="none" stroke="currentColor" strokeWidth={1.8} viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15M12 9l3 3m0 0l-3 3m3-3H2.25" />
          </svg>
        </span>
        <LogoutButton />
      </div>
    </nav>
  )
}
